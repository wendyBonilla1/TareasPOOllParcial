package com.mycompany.tareamodulo4;

public class Futbolista {

    private Integer edad;
    private String nombre;
    private String equipoActual;

    void setEdad(Integer edad) {
        this.edad = edad;
    }

    Integer getEdad() {
        return this.edad;
    }

    void setNombre(String nombre) {
        this.nombre = nombre;
    }

    String getNombre() {
        return this.nombre;
    }

    void setEquipoActual(String equipoActual) {
        this.equipoActual = equipoActual;
    }

    String getEquipoActual() {
        return this.equipoActual;
    }

}
